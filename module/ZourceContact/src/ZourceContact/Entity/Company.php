namespace ZourceContact\Entity;

use Doctrine\ORM\Mapping as ORM;

class Company extends AbstractContact
{
    /**
     * @var string
     */
    private $name;
    
    public function __construct($name)
    {
        parent::__construct();

        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}
